public class VariablesJava {
    public static void main(String args[]){
        // Definimos la variable
        int miVariableEntera =  10;
        System.out.println(miVariableEntera);
        // Modificamos el valor de la variable
        miVariableEntera = 5;
        System.out.println(miVariableEntera); // sout y luego tab, netbeans autocompleta toda la linea de impresion
        
        String miVariableCadena = "Saluditos :D";
        System.out.println(miVariableCadena); // Ctrl + espacio, nos indica el IDE las variables que hay (para autocompletar)
        
        miVariableCadena = "Bai bai :D";
        System.out.println(miVariableCadena);
    
    }
}
